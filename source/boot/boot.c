
__asm__(".code16gcc");

#include "boot.h"

#define LOADER_START_ADDR 0x8000

/*************************************************************************
 * loader的C入口函数
 * 只完成一项功能，在汇编中从磁盘找到loader文件然后加载到内容后，跳转过去
 ************************************************************************/
void boot_entry(void) {
    //把地址强转成函数指针指向一个地址，将地址作为函数跳转
    ((void (*)(void))LOADER_START_ADDR)();
} 

